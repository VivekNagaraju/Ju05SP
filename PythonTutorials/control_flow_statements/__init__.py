'''
Control flow statements:

Statements which control the flow of execution of a program

Types of controls:
1. Sequential flow: All statements are executed one after another
2. Conditional flow: Certain set of statements are executed if a condition is satisfied (based on a condition)
    - If condition gives you True as result one set of statements will be executed
    - If condition gives you False as result another set of statements will be executed
    - Conditional statements will executed only once per execution
3. Looping: A set of statements will be executed repeatedly until a condition is fulfilled
'''